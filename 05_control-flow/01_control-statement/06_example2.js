let age = 20;

// if-else example

if (age >= 18) {
    console.log("You are eligible for driving!!");
}
else {
    console.log("You are not eligible for driving!!");
}


// ternary example

age = 20;

age >= 18
    ? console.log("You are eligible for driving!!")
    : console.log("You are not eligible for driving!!");