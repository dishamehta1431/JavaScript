// iife stand for "Immediately Invoked Function Expression"

(function () {
  console.log("This function runs immediately after it's defined!");
})();

// example 1

(substraction = (a, b) => {
  console.log(a - b);
})(14, 31);

// example 2

(multiplication = (a, b) => {
  console.log(a * b);
})(31, 14);



